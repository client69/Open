
public class PrintAlphabets 
{

	public static void main(String[] args) 
	{
		
		System.out.println("Upper Case Letters are ");
		for(char i='A';i<='Z';i++)
		{
			System.out.print(i+" ");
		}
		System.out.println();
		System.out.println("Lower  Case Letters are ");
		for(char i='a';i<='z';i++)
		{
			System.out.print(i+" ");
		}
	}

}
