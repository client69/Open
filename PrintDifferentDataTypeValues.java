
public class PrintDifferntTypesOfValues
{

	public static void main(String[] args) 
	{
		int num;
		float numf;
		double numd;
		char chars;
		String s;
		
		num=12;
		numf=12.34f;
		numd=45.023;
		chars='s';
		s="Java Coding";
		
		System.out.println("Integer is "+num);
		System.out.println("float is "+numf);
		System.out.println("Double is "+numd);
		System.out.println("Character is "+chars);
		System.out.println("String is "+s);
		

	}

}
